class PrimitivesTest {


    public static void main(String[] args) {
        int age = 23;
        System.out.println("The age is " + age);

        long population = 8_000_000_000L;
        System.out.println("The population is " + population);

        double bladeLength  = 1.75;
        System.out.println("The blade length is " + bladeLength);

        boolean isStressed = false;
        System.out.println("This programmer is stressed: " + isStressed);

        char size = 'M';
        System.out.println("The size of this drink is " + size);
        String grade = "F";
        System.out.println("James will NEVER get a " + grade + " in this class");

        String quote = "Whats up, \"Doc\"";
        System.out.println("Buggs bunny often says " + quote);

        System.out.println(); //blank line in the output
        int i = 18;
        int j = i + 2;
        System.out.println(j);
        i = 55;
        System.out.println(j);

    }

}